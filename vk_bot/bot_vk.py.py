def normalize_ingredient(ing):
    ing = ing.lower().strip()
    if ing in SYNONYMS:
        return SYNONYMS[ing]
    return ing

def parse_user_products(text):
    text = text.lower()
    text = re.sub(r'[⭐🔙📝❓🍳📋✅🔴🟡🟢]', '', text)
    
    patterns = [
        r"список продуктов[:\s]+(.*)",
        r"продукты[:\s]+(.*)",
        r"есть[:\s]+(.*)",
        r"имеется[:\s]+(.*)",
    ]
    
    products_str = text
    for pattern in patterns:
        match = re.search(pattern, text)
        if match:
            products_str = match.group(1)
            break
    
    if "," in text and not any(cmd in text for cmd in ["помощь", "рецепты", "назад"]):
        products_str = text
    
    products = re.split(r'[,;\sи+]+', products_str)
    products = [normalize_ingredient(p) for p in products if p and len(p) > 1]
    return list(set(products))

def find_recipes(available_products):
    available = set(available_products)
    matched = []
    
    for recipe in RECIPES:
        required = set(recipe["ingredients"])
        missing = required - available
        missing_count = len(missing)
        
        if missing_count == 0:
            matched.append((recipe, 0, []))
        elif missing_count <= 2:
            matched.append((recipe, missing_count, list(missing)))
    
    matched.sort(key=lambda x: (x[1], x[0]["difficulty"]))
    return matched

