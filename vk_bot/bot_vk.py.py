def format_response(matched_recipes):
    if not matched_recipes:
        return "😕 Ничего не найдено. Попробуйте другие продукты.\n\n📌 Пример: яйца, мука, молоко"
    
    full_recipes = [r for r in matched_recipes if r[1] == 0]
    close_recipes = [r for r in matched_recipes if r[1] > 0]
    
    msg = "🍳 **Что можно приготовить:**\n\n"
    
    if full_recipes:
        msg += "✅ **Можете приготовить прямо сейчас:**\n\n"
        for recipe, _, _ in full_recipes[:10]:
            stars = "⭐" * recipe["difficulty"]
            msg += f"• **{recipe['name']}** {stars} — {recipe['time']}\n"
        msg += "\n"
    
    if close_recipes:
        msg += "🟡 **Почти готово!** (не хватает 1-2 продуктов):\n\n"
        for recipe, _, missing in close_recipes[:5]:
            stars = "⭐" * recipe["difficulty"]
            msg += f"• **{recipe['name']}** {stars} — {recipe['time']}\n"
            msg += f"  ❌ Не хватает: {', '.join(missing)}\n"
    
    return msg

