def format_all_recipes():
    msg = f"📖 **Все рецепты ({len(RECIPES)}):**\n\n"
    
    for diff in [1, 2, 3]:
        diff_name = {1: "🟢 ПРОСТЫЕ", 2: "🟡 СРЕДНИЕ", 3: "🔴 СЛОЖНЫЕ"}[diff]
        diff_recipes = [r for r in RECIPES if r["difficulty"] == diff]
        
        if diff_recipes:
            msg += f"**{diff_name} ({len(diff_recipes)}):**\n"
            for r in diff_recipes[:15]:
                msg += f"• {r['name']} — {r['time']}\n"
            if len(diff_recipes) > 15:
                msg += f"  ... и ещё {len(diff_recipes)-15} блюд\n"
            msg += "\n"
    
    return msg

